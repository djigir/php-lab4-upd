<?php
require_once '../translate/translate.php'; // файл с переводами

$lang = $_POST['lang'];

$_SESSION['lang'] = $lang;

abstract class User {

    protected $_users = [[
    ]];

    protected $translate;

    // конструктор

    function __construct($users, $translate) {

        $this->_users = $users;

        $this->translate = $translate;
    }


    public  function sayHello($role) {

        return $this->translate[$_SESSION['lang']]['default'] . " <strong>{$this->_users['role']}\n</strong>" ."{$this->_users['name']} {$this->_users['surname']}\n" . "{$this->translate[$_SESSION['lang']][$role]}\n";
    }

}
